/* division and modulus operation in c */

#include <stdio.h>

int main()
{
	printf("103 %% 10 = %d\n", 103 % 10);
	printf("-103 %% 10 = %d\n", -103 % 10);
	printf("103 %% -10 = %d\n", 103 % -10);
	printf("-103 %% -10 = %d\n", -103 % -10);
	
	printf("103 / 10 = %d\n", 103 / 10);
	printf("-103 / 10 = %d\n", -103 / 10);
	printf("103 / -10 = %d\n", 103 / -10);
	printf("-103 / -10 = %d\n", -103 / -10);
}
